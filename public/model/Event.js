export class Event {
    uid = null; //generated by firebase
    title = null;
    description = null;
    reminder = null; // time, how (phone, email)
    category = null;
    location = null;
    type = null; //deadline?
    date = null;
    time = null;
    repeat = null; // would this be complex, cause like amount of time, when
    docId = null;

    constructor(data) {
        this.uid = data.uid;
        this.title = data.title;
        this.description = data.description;
        this.reminder = data.reminder;
        this.category = data.category;
        this.location = data.location;
        this.type = data.type;
        this.date = data.date;
        this.time = data.time;
        this.repeat = data.repeat;
    }

    set_docId(docId) {
        this.docId = docId;
    }

    toFirestore() { //this function converts the data of the model into something the firestore database understands /can store
        return {
            uid: this.uid,
            title: this.title,
            description: this.description,
            reminder: this.reminder,
            category: this.category,
            location: this.location,
            type: this.type,
            date: this.date,
            time: this.time,
            repeat: this.repeat
        };
    }
}