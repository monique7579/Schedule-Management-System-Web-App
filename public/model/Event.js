export class Event {
    uid = null; //generated by firebase, user id because events will be per user
    title = null; //name/title of event
    description = null; 
    category = null; //category which will correspond to the categories the user has
    start = null; //start date && time
    finish = null; //finish date && time
    reminderBool = null; //boolean, reminder yes or no
    reminderTime = null; //when is the reminder (from a series of drop down choices)
    docId = null;

    constructor(data) {
        this.uid = data.uid
        this.title = data.title;
        this.description = data.description;
        this.category = data.category;
        this.start = data.start;
        this.finish = data.finish;
        this.reminderBool = data.reminderBool;
        this.reminderTime = data.reminderTime;
    }

    set_docId(docId) {
        this.docId = docId;
    }

    //this function converts the data of the model into something the firestore database understands/can store
    toFirestore() {
        return {
            uid: this.uid,
            title: this.title,
            description: this.description,
            category: this.category,
            start: this.start,
            finish: this.finish,
            reminderBool: this.reminderBool,
            reminderTime: this.reminderTime
        };
    }
}